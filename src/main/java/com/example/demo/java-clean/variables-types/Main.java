// Types

// int - whole numbers, like -5, 1, 0, 2000
// long - whole numbers, like 2,633,001,012,485,774
// double - floating numbers, -1.1, 3.14,215.67
// boolean - true or false
// char - like 'a', 'b', '7', '$'
// byte, short, float

public class Main {
    public static void main(String[] args) {
        System.out.print("Hello, World!");

        // Variables
        int age;
        double price;
        boolean isAdult;
        char initial;

        // variableName = value;
        age = 30;
        price = 19.99;
        isAdult = true;
        initial = 'J';

        int score = 100;
        double temperature = 25.5;
        char special = '!';

        System.out.println(score);
        System.out.println(temperature);
        System.out.println(special);
    }
}